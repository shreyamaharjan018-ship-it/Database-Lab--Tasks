-- Q1: 10% Salary Raise for Research Department
SELECT E.FNAME, E.LNAME,
E.SALARY * 1.1 AS increased_salary
FROM EMPLOYEE E
JOIN DEPARTMENT D ON E.DNO = D.DNUMBER
WHERE D.DNAME = 'Research';


-- Q2: Salary Statistics of Administration Department
SELECT SUM(E.SALARY) AS Total,
MAX(E.SALARY) AS Max_Salary,
MIN(E.SALARY) AS Min_Salary,
AVG(E.SALARY) AS Average_Salary
FROM EMPLOYEE E
JOIN DEPARTMENT D ON E.DNO = D.DNUMBER
WHERE D.DNAME = 'Administration';


-- Q3: Employees Controlled by Department 5
SELECT FNAME, LNAME
FROM EMPLOYEE
WHERE DNO = 5;


-- Q4: Departments having at least 2 employees
SELECT D.DNAME, COUNT(*) AS emp_count
FROM EMPLOYEE E
JOIN DEPARTMENT D
ON E.DNO = D.DNUMBER
GROUP BY D.DNAME
HAVING COUNT(*) >= 2;


-- Employees born between 1990 and 1999
SELECT *
FROM EMPLOYEE
WHERE BDATE BETWEEN DATE '1990-01-01' AND DATE '1999-12-31';


